import kotlin.math.round

fun main() {
    val num1 = 2.4
    val num2 = 2.5
    val num3 = 2.6

    val rounded1 = round(num1)
    val rounded2 = round(num2)
    val rounded3 = round(num3)

    println(rounded1)  // Output: 2.0
    println(rounded2)  // Output: 3.0
    println(rounded3)  // Output: 3.0
}